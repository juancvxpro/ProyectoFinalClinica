    private String encript(String cadena) {
        String encript = "";
        cargarLetras();
        cargarNumeros();
        for (int i = 0; i < cadena.length(); i++) {
            for (Map.Entry<Character, Character> entry : map.entrySet()) {
                char c = cadena.charAt(i);
        
                if (c==entry.getValue()) {

                    encript += entry.getKey();

                }

            }
        }
        return encript;

    }

