    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       String mensaje="";
        System.out.println(id+" "+u);
        if(validarCampos()||id!=0){
            int d = JOptionPane.showConfirmDialog(this, "ESTA SEGURO DE ELIMINAR AL SERVICIO " + txtNombre.getText());
            if (d == JOptionPane.YES_OPTION) {
               
                 mensaje = serviciosBO.eliminarServicio(id, u);
                 JOptionPane.showMessageDialog(this, mensaje);
                  cargarServiciosTbl();
               
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       String p="";
        if(rbID2.isSelected()){
            p="ID";
            System.out.println(p);
        }
        if (rbNombre.isSelected()){
            p="NOMBRE";
            System.out.println(p);
        }

        
        Servicios s = new Servicios();
        
    
        String opcion = "";

        if(p.equals("ID")){
            opcion= JOptionPane.showInputDialog(null,"Ingrese ID");
          
            s.setSer_codigo(Integer.parseInt(opcion));
        }
        if(p.equals("NOMBRE")){
            opcion= JOptionPane.showInputDialog(null,"Ingrese nombre");
           s.setSer_nombre(opcion);
        }
          System.out.println(opcion);
          if(!"".equals(opcion)|| u!=null||!"".equals(p)){
            System.out.println(opcion+" "+p+" "+u);
            serviciosBO.buscarServicios(s, u, tblServicios, p);
            }else{
             JOptionPane.showMessageDialog(this,"SELECCIONE TIPO DE BUSQUEDA");
            }
    }//GEN-LAST:event_btnBuscarActionPerformed

