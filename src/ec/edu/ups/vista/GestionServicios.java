    public boolean validarCampos() {
     
     if( txtNombre.getText().isEmpty()||
             txtPrecio.getText().isEmpty()||
             txtIVA.getText().isEmpty()) {
           JOptionPane.showMessageDialog(this, "CAMPOS VACIOS");
     return false;
     
     }else{
     return true;
     }

    }

    public void cargarServiciosTbl() {
        
        if(u!=null){
         serviciosBO.listarServicios(tblServicios);
        }else{
            System.out.println("TABLA VACIA:SERVICIOS");
        }
       
        System.out.println(u);

    }
    public boolean isNumeric(String a){
        try {
		Integer.parseInt(a);
		return true;
	} catch (NumberFormatException nfe){
		return false;
	}
    
    
    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       String mensaje="";
        Servicios s = new Servicios();       
        if(validarCampos()){
            if(isNumeric(txtPrecio.getText())&&isNumeric(txtIVA.getText())){
            //No se agrega el id por que se esta utilizando secuencias
       s.setSer_nombre(txtNombre.getText());
       s.setSer_precio(Double.parseDouble(txtPrecio.getText()));
       s.setSer_iva(Double.parseDouble(txtIVA.getText()));
        // mensaje = pBO.agregarPersona(p,u);
       mensaje= serviciosBO.agregarServicio(s, u);
       JOptionPane.showMessageDialog(this, mensaje);
     cargarServiciosTbl();
            }else{
            JOptionPane.showMessageDialog(this, "INGRESE VALORES NUMERICOS EN PRECIO E IVA");
            }
        
       }
        System.out.println(s+" "+u);
        System.out.println(mensaje);
    }//GEN-LAST:event_btnGuardarActionPerformed

